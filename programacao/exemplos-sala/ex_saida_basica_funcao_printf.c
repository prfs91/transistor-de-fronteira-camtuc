#include<stdio.h>

/*
_________________________________________________________
|   Código printf() |   Formato                         |
|-------------------|-----------------------------------|
|   %c              |   caractere simples               |
|   %d              |   decimal                         |
|   %f              |   ponto flutuante                 |
|   %s              |   cadeia de caracteres            |
|   %lf             |   ponto flutuante longo (double)  |
---------------------------------------------------------
*/

int main(void)
{

    printf("Teste geral\n");
    // → Teste geral

    printf("Esta turma tem quase %d alunos\n", 60);
    // → Esta turma tem quase 60 alunos  
    
    
}